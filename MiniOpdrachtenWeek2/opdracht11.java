package MiniOpdrachtenWeek2;

public class opdracht11 {
	public static void main(String[] args) {

		// opdracht 11
		int[] arr = { 1, 2, 3, 4, 10, 11 };
		int sum = 0;

		// assign sum of elements of array arr to variable sum
		for (int i : arr) {
			sum += i;

		}

		// print variable sum
		System.out.println(sum);
	}
}